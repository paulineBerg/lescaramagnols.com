document.addEventListener("DOMContentLoaded", function () {
  const hamburgerIcon = document.getElementById("hamburger-icon");
  const breadcrumbMenu = document.querySelector(".breadcrumb-menu");

  // Gestion de l'affichage du menu au clic
  hamburgerIcon.addEventListener("click", function () {
    const isMenuVisible = breadcrumbMenu.style.display === "block";
    breadcrumbMenu.style.display = isMenuVisible ? "none" : "block";
  });

  // Optionnel : Masquer le menu si on clique ailleurs sur la page
  document.addEventListener("click", function (event) {
    if (!breadcrumbMenu.contains(event.target) && !hamburgerIcon.contains(event.target)) {
      breadcrumbMenu.style.display = "none";
    }
  });
})
</script> 
<script>       
 document.addEventListener("DOMContentLoaded", function () {
  const breadcrumbMenu = document.querySelector(".breadcrumb-menu");

  // Gérer le clic sur les éléments avec sous-menus
  breadcrumbMenu.addEventListener("click", function (event) {
    const parentLi = event.target.closest("li.has-submenu");
    
    if (parentLi) {
      event.preventDefault(); // Empêche la navigation par défaut
      parentLi.classList.toggle("open"); // Ouvre ou ferme le sous-menu
    }
  });

  // Gérer les liens sans sous-menu
  breadcrumbMenu.querySelectorAll("li:not(.has-submenu) > a").forEach(link => {
    link.addEventListener("click", function () {
      window.location.href = link.href; // Navigation normale
    });
  });
});
</script> 
