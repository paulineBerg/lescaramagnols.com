<script>
    document.addEventListener('DOMContentLoaded', function () {
        const menuItems = document.querySelectorAll('#menu2 li');

        menuItems.forEach(function (menuItem) {
            const subMenu = menuItem.querySelector('ul');
            if (subMenu) {
                menuItem.querySelector('a').addEventListener('click', function (e) {
                    e.preventDefault(); // Empêche la navigation par défaut du lien
                    
                    // Ferme tous les autres sous-menus
                    menuItems.forEach(function (otherItem) {
                        if (otherItem !== menuItem) {
                            otherItem.classList.remove('active');
                        }
                    });
                    
                    // Active ou désactive le sous-menu actuel
                    menuItem.classList.toggle('active');
                });
            }
        });

        // Optionnel : Fermer tous les menus en cliquant à l'extérieur
        document.addEventListener('click', function (e) {
            if (!e.target.closest('#menu2')) {
                menuItems.forEach(function (menuItem) {
                    menuItem.classList.remove('active');
                });
            }
        });
    });
</script> 
